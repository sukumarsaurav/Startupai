// Function to get SEO data based on category and service
function get_seo_data($category, $service = null, $subservice = null) {
    global $seo_config;
    
    if ($category == 'home') {
        return $seo_config['home'];
    }
    
    if (!empty($category) && !empty($service) && !empty($subservice)) {
        // For nested sub-services (e.g., services/digital-marketing/seo)
        if (isset($seo_config[$category][$service][$subservice])) {
            return $seo_config[$category][$service][$subservice];
        }
    }
    
    if (!empty($category) && !empty($service)) {
        // For services with subcategories (e.g., services/web-design)
        if (isset($seo_config[$category][$service])) {
            return $seo_config[$category][$service];
        }
    }
    
    if (!empty($category)) {
        // For main categories (e.g., services, blog, about)
        if (isset($seo_config[$category])) {
            return $seo_config[$category];
        }
    }
    
    // Default fallback
    return [
        'title' => 'StartupAI | Your Technology Partner',
        'description' => 'StartupAI provides innovative technology solutions for businesses of all sizes.',
        'keywords' => 'StartupAI, technology, digital solutions, innovation'
    ];
}
